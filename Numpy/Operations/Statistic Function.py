import numpy as np

np.random.seed(1)
x = np.random.randint(1, 100, 11)
print(f"Original array = {x}")

print(f"Sum = {x.sum()}")
print(f"Mean = {x.mean()}")
print(f"Max = {x.max()}")
print(f"Min = {x.min()}")
print(f"Variance = {x.var()}")
print(f"Standard Deviation = {x.std()}")
print(f"Standard Deviation = {x.std()}")
